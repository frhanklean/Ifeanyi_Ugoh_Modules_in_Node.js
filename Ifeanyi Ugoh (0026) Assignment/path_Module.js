/* 
DESCRIPTION: The path module provides methods for working with file paths in a way that
             is consistent across different operating systems. Here's an example of joining
             two paths with the path module:
*/

//EXAMPLE:

const path = require('path');

const fullPath = path.join('/path/to', 'file.txt');

console.log(fullPath); // Output: "/path/to/file.txt"































